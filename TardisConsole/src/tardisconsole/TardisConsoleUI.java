    private void btnConnectActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConnectActionPerformed
        // TODO add your handling code here:
        if (evt.getActionCommand() == "Connect") {
            btnConnect.setActionCommand("Disconnect");
            btnConnect.setText("Disconnect");
            try {
                int baud = Integer.valueOf(jComboBox1.getSelectedItem().toString());
                ti = new TardisInterface(jComboBox2.getSelectedItem().toString(), baud);
                // Set button to a dissconnect button....
                // If all connected ok then enable the tardis controls
                enableTardis(true);
                
                // NOT WORKING because read data will not be received at this point, considder setting up
                // a timed loop to update teh temp on the screen. 
                jLabel1.setText(ti.getTemp()+"Â°C");
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, e.toString()
                    ,"Error: Cannot connect to TARDIS", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            btnConnect.setActionCommand("Connect");
            btnConnect.setText("Connect");
            ti.close();
            ti = null;
            enableTardis(false);
        }
    }//GEN-LAST:event_btnConnectActionPerformed

